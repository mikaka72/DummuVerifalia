package com.cdk.verifalia.model;

public class Progress {

	private int noOfTotalEntries;
	private int noOfCompletedEntries;
	
	
	public int getNoOfTotalEntries() {
		return noOfTotalEntries;
	}
	public void setNoOfTotalEntries(int noOfTotalEntries) {
		this.noOfTotalEntries = noOfTotalEntries;
	}
	public int getNoOfCompletedEntries() {
		return noOfCompletedEntries;
	}
	public void setNoOfCompletedEntries(int noOfCompletedEntries) {
		this.noOfCompletedEntries = noOfCompletedEntries;
	}
	
	
}
